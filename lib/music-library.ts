// Music search and filtering functions
export function searchMusic(
  query: string,
  filters?: {
    genre?: string
    language?: string
    mood?: string
    artist?: string
  },
): MusicTrack[] {
  let results = MUSIC_LIBRARY

  // Text search
  if (query) {
    const searchTerm = query.toLowerCase()
    results = results.filter(
      (track) =>
        track.title.toLowerCase().includes(searchTerm) ||
        track.artist.toLowerCase().includes(searchTerm) ||
        track.tags.some((tag) => tag.toLowerCase().includes(searchTerm)),
    )
  }

  // Apply filters
  if (filters?.genre) {
    results = results.filter((track) => track.genre.toLowerCase() === filters.genre?.toLowerCase())
  }
  if (filters?.language) {
    results = results.filter((track) => track.language.toLowerCase() === filters.language?.toLowerCase())
  }
  if (filters?.mood) {
    results = results.filter((track) => track.mood.toLowerCase() === filters.mood?.toLowerCase())
  }
  if (filters?.artist) {
    results = results.filter((track) => track.artist.toLowerCase().includes(filters.artist?.toLowerCase() || ""))
  }

  return results
}

export function getRandomTrack(genre?: string): MusicTrack {
  let tracks = MUSIC_LIBRARY
  if (genre) {
    tracks = tracks.filter((track) => track.genre.toLowerCase() === genre.toLowerCase())
  }
  return tracks[Math.floor(Math.random() * tracks.length)]
}

export function getTracksByGenre(genre: string): MusicTrack[] {
  return MUSIC_LIBRARY.filter((track) => track.genre.toLowerCase() === genre.toLowerCase())
}

export function getPopularTracks(limit = 50): MusicTrack[] {
  // Return tracks from recent years (more popular)
  return MUSIC_LIBRARY.filter((track) => (track.year || 2000) >= 2015)
    .sort(() => Math.random() - 0.5)
    .slice(0, limit)
}

export function getMusicStats() {
  const stats = {
    totalTracks: MUSIC_LIBRARY.length,
    genres: [...new Set(MUSIC_LIBRARY.map((track) => track.genre))].length,
    languages: [...new Set(MUSIC_LIBRARY.map((track) => track.language))].length,
    artists: [...new Set(MUSIC_LIBRARY.map((track) => track.artist))].length,
    byGenre: {} as Record<string, number>,
  }

  // Count tracks by genre
  MUSIC_LIBRARY.forEach((track) => {
    stats.byGenre[track.genre] = (stats.byGenre[track.genre] || 0) + 1
  })

  return stats
}
